import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const apiDir = path.resolve(__dirname, '../src/app/api');
const outputDir = path.resolve(__dirname, '../__create');
const outputFile = path.join(outputDir, 'static-routes-registry.ts');

function findRouteFiles(dir) {
  const files = fs.readdirSync(dir);
  let routes = [];

  for (const file of files) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);

    if (stat.isDirectory()) {
      routes = routes.concat(findRouteFiles(filePath));
    } else if (file === 'route.js') {
      routes.push(filePath);
    }
  }

  return routes;
}

function getHonoPath(routeFile) {
  const relativePath = path.relative(apiDir, routeFile).replace(/\\/g, '/');
  const parts = relativePath.split('/').filter(Boolean);
  const routeParts = parts.slice(0, -1); // Remove 'route.js'
  if (routeParts.length === 0) {
    return '/';
  }
  const transformedParts = routeParts.map((segment) => {
    const match = segment.match(/^\[(\.{3})?([^\]]+)\]$/);
    if (match) {
      const [_, dots, param] = match;
      return dots === '...'
        ? `:${param}{.+}`
        : `:${param}`;
    }
    return segment;
  });
  return '/' + transformedParts.join('/');
}

function generate() {
  console.log('Generating static routes registry...');
  if (!fs.existsSync(apiDir)) {
    console.error(`API directory ${apiDir} does not exist`);
    process.exit(1);
  }

  const routeFiles = findRouteFiles(apiDir).sort((a, b) => b.length - a.length);
  
  let importsContent = '';
  let registrationContent = 'export function registerStaticRoutes(api: any) {\n';

  routeFiles.forEach((file, index) => {
    const relativeImportPath = path.relative(outputDir, file).replace(/\\/g, '/');
    const importName = `route${index}`;
    importsContent += `import * as ${importName} from '${relativeImportPath}';\n`;

    const honoPath = getHonoPath(file);
    const methods = ['GET', 'POST', 'PUT', 'DELETE', 'PATCH'];

    methods.forEach((method) => {
      registrationContent += `  if ((${importName} as any).${method}) {\n`;
      registrationContent += `    api.${method.toLowerCase()}('${honoPath}', async (c: any) => {\n`;
      registrationContent += `      const params = c.req.param();\n`;
      registrationContent += `      return await (${importName} as any).${method}(c.req.raw, { params });\n`;
      registrationContent += `    });\n`;
      registrationContent += `  }\n`;
    });
  });

  registrationContent += '}\n';

  const fileContent = `// This file is auto-generated by generate-static-routes.js\n// Do not edit this file manually.\n\n${importsContent}\n${registrationContent}`;

  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(outputFile, fileContent);
  console.log(`Successfully generated static routes registry at ${outputFile}`);
}

generate();
